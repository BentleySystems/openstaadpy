class OSCommand:
    CoInitialize()

    def __init__(self, staadObj):
        self._staad = staadObj
        self._command = self._staad.Command

        self._functions = [
            "PerformAnalysis",
            "PerformPDeltaAnalysisNoConverge",
            "CreateSteelDesignCommand",
            "PerformCableAnalysis",
            "PerformBucklingAnalysis",
            "SetFloorDiaphragmBaseCommand",
            "DeleteFloorDiaphragmBaseCommand",
            "SetCheckSoftStoryCommand",
            "DeleteCheckSoftStoryCommand",
            "SetCheckIrregularitiesCommand",
            "DeleteCheckIrregularitiesCommand",
            "PerformBucklingAnalysisEx",
            "PerformCableAnalysisEx",
            "PerformDirectAnalysis",
            "PerformNonlinearAnalysisEx",
            "DeleteAllAnalysisCommands",
            "PerformPDeltaAnalysisEx",
        ]

        for function_name in self._functions:
            self._command._FlagAsMethod(function_name)

    def PerformAnalysis(self, printOption: int):
        """
        Perform a standard analysis on the current STAAD model.

        Parameters
        ----------
        printOption : int
            The print option for the analysis results. Possible values are:
                +-------+---------------------+
                | Value | Print Option        |
                +=======+=====================+
                | 1     | Print Load Data     |
                +-------+---------------------+
                | 2     | Print Statics Check |
                +-------+---------------------+
                | 3     | Print Statics Load  |
                +-------+---------------------+
                | 4     | Print Mode Shapes   |
                +-------+---------------------+
                | 5     | Print Both          |
                +-------+---------------------+
                | 6     | Print All           |
                +-------+---------------------+
                | 0     | No Print            |
                +-------+---------------------+

        Returns
        -------
        None

        Examples
        --------
        >>> from openstaadpy import os_analytical
        >>> staad_obj = os_analytical.connect()
        >>> staad_obj.Command.PerformAnalysis()
        """
        self._command.PerformAnalysis(printOption)

    def PerformPDeltaAnalysisNoConverge(self, NoOfIterations: int, PrintOption: int):
        """
        Perform a P-Delta analysis without convergence check.

        Parameters
        ----------
        NoOfIterations : int
            The number of iterations to perform.
        PrintOption : int
            The print option for the analysis results. Possible values are:
                +-------+---------------------+
                | Value | Print Option        |
                +=======+=====================+
                | 1     | Print Load Data     |
                +-------+---------------------+
                | 2     | Print Statics Check |
                +-------+---------------------+
                | 3     | Print Statics Load  |
                +-------+---------------------+
                | 4     | Print Mode Shapes   |
                +-------+---------------------+
                | 5     | Print Both          |
                +-------+---------------------+
                | 6     | Print All           |
                +-------+---------------------+
                | 0     | No Print            |
                +-------+---------------------+

        Returns
        -------
        None

        Examples
        --------
        >>> from openstaadpy import os_analytical
        >>> staad_obj = os_analytical.connect()
        >>> staad_obj.Command.PerformPDeltaAnalysisNoConverge(5, 1)
        """
        self._command.PerformPDeltaAnalysisNoConverge(NoOfIterations, PrintOption)

